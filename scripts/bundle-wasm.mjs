#!/usr/bin/env node
// bundle-wasm.mjs — embed driver/zig-out/bin/tobiifree_core.wasm as a base64
// string in sdk/src/wasm-bundle.ts so browser bundlers don't need to ship
// a separate .wasm asset. Run after `zig build` in driver/.

import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');
const wasmPath = resolve(root, 'driver/zig-out/bin/tobiifree_core.wasm');
const outPath = resolve(root, 'sdk/src/wasm-bundle.ts');

const wasm = await readFile(wasmPath);
const b64 = wasm.toString('base64');

const contents = `// Auto-generated by scripts/bundle-wasm.mjs. Do not edit.
// Source: driver/zig-out/bin/tobiifree_core.wasm (${wasm.byteLength} bytes)

const BASE64 = '${b64}';

export function wasmBytes(): Uint8Array<ArrayBuffer> {
  const bin = atob(BASE64);
  const buf = new ArrayBuffer(bin.length);
  const out = new Uint8Array(buf);
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);
  return out;
}
`;

await writeFile(outPath, contents);
console.log(`wrote ${outPath} (${wasm.byteLength} bytes wasm → ${b64.length} b64 chars)`);
